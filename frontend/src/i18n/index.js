// Map BCP-47 code to translation group
function getLangGroup(code) {
  if (!code) return 'en'
  if (code.startsWith('ar')) return 'ar'
  if (code.startsWith('fr')) return 'fr'
  if (code.startsWith('es')) return 'es'
  if (code.startsWith('de')) return 'de'
  if (code.startsWith('it')) return 'it'
  if (code.startsWith('pt')) return 'pt'
  if (code.startsWith('ru')) return 'ru'
  if (code.startsWith('tr')) return 'tr'
  if (code.startsWith('nl')) return 'nl'
  if (code.startsWith('af')) return 'af'
  return 'en'
}

export function getAppLang() {
  return localStorage.getItem('spendly_lang_app') ||
         localStorage.getItem('spendly_lang_response') || // migrate old key
         'en-US'
}

export function isRTL() {
  return getAppLang().startsWith('ar')
}

export function t(key) {
  const lang = getLangGroup(getAppLang())
  return T[lang]?.[key] ?? T['en'][key] ?? key
}
